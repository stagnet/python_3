import  turtle


my_jango = turtle.Turtle()

def  pentagon(length,angle):
    my_jango.forward(length)
    my_jango.right(angle)
    my_jango.forward(length)
    my_jango.right(angle)
    my_jango.forward(length)
    my_jango.right(angle)
    my_jango.forward(length)
    my_jango.right(angle)
    my_jango.forward(length)
    my_jango.right(angle)



pentagon(125,145)
pentagon(125,145)
pentagon(125,145)
